#!/bin/bash
set -euo pipefail

HERMES_HOME="${HERMES_HOME:-/data/.hermes}"
SKELETON_DIR="/home-skeleton"

echo "=== Hermes Agent Entrypoint ==="
echo "HERMES_HOME: $HERMES_HOME"

# ── Fix volume ownership (Umbrel creates volumes as root) ────────────────────
sudo chown -R "$(id -u):$(id -g)" /data /home/linuxbrew 2>/dev/null || true

# ── Initialize home directory from skeleton (first run) ──────────────────────
if [ -d "$SKELETON_DIR" ]; then
    for item in "$SKELETON_DIR"/* "$SKELETON_DIR"/.*; do
        basename=$(basename "$item")
        [ "$basename" = "." ] || [ "$basename" = ".." ] && continue
        [ ! -e "$item" ] && continue
        # linuxbrew is mounted as a separate volume at /home/linuxbrew
        [ "$basename" = "linuxbrew" ] && continue

        dest="$HOME/$basename"
        if [ ! -e "$dest" ]; then
            echo "Copying $basename to home..."
            cp -r "$item" "$dest"
        fi
    done
    echo "Home directory initialized"
fi

# Initialize linuxbrew volume if empty (separate mount point)
if [ -d "$SKELETON_DIR/linuxbrew" ] && [ ! -f "/home/linuxbrew/.linuxbrew/bin/brew" ]; then
    echo "Initializing linuxbrew volume..."
    cp -r "$SKELETON_DIR/linuxbrew/." /home/linuxbrew/
    echo "Linuxbrew initialized"
fi

# ── Create required directories ──────────────────────────────────────────────
mkdir -p "$HERMES_HOME"/{sessions,logs,pairing,hooks,image_cache,audio_cache,memories,skills,whatsapp,cron}

# ── Source environment from setup server config ──────────────────────────────
if [ -f "$HERMES_HOME/.env" ]; then
    echo "Loading environment from $HERMES_HOME/.env"
    set -a
    # shellcheck disable=SC1091
    source "$HERMES_HOME/.env"
    set +a
fi

if [ -f "$HERMES_HOME/.env.local" ]; then
    set -a
    # shellcheck disable=SC1091
    source "$HERMES_HOME/.env.local"
    set +a
fi

# ── Pre-approve Telegram home chat (skip pairing for the owner) ──────────────
if [ -n "${TELEGRAM_HOME_CHAT_ID:-}" ]; then
    approved_file="$HERMES_HOME/pairing/telegram-approved.json"
    if [ ! -f "$approved_file" ] || ! grep -q "\"${TELEGRAM_HOME_CHAT_ID}\"" "$approved_file" 2>/dev/null; then
        echo "Pre-approving Telegram home chat: $TELEGRAM_HOME_CHAT_ID"
        mkdir -p "$HERMES_HOME/pairing"
        # Merge with existing approvals if file exists
        if [ -f "$approved_file" ]; then
            python3 -c "
import json, time
with open('$approved_file') as f: d = json.load(f)
d['${TELEGRAM_HOME_CHAT_ID}'] = {'user_name': 'owner', 'approved_at': time.time(), 'approved_via': 'entrypoint'}
with open('$approved_file', 'w') as f: json.dump(d, f, indent=2)
"
        else
            echo "{\"${TELEGRAM_HOME_CHAT_ID}\": {\"user_name\": \"owner\", \"approved_at\": $(date +%s), \"approved_via\": \"entrypoint\"}}" > "$approved_file"
        fi
        # Clear any rate limits for this user
        rate_file="$HERMES_HOME/pairing/_rate_limits.json"
        if [ -f "$rate_file" ]; then
            python3 -c "
import json
with open('$rate_file') as f: d = json.load(f)
d = {k:v for k,v in d.items() if not k.endswith(':${TELEGRAM_HOME_CHAT_ID}')}
with open('$rate_file', 'w') as f: json.dump(d, f, indent=2)
" 2>/dev/null || true
        fi
    fi
fi

# ── Generate config.yaml if needed ───────────────────────────────────────────
config_file="$HERMES_HOME/config.yaml"

if [ ! -f "$config_file" ] || [ "${HERMES_REGEN_CONFIG:-}" = "true" ]; then
    echo "Generating config.yaml..."

    model="${HERMES_MODEL:-anthropic/claude-sonnet-4-20250514}"
    provider="${HERMES_PROVIDER:-auto}"

    # Build gateway platforms section
    platforms=""

    if [ -n "${TELEGRAM_BOT_TOKEN:-}" ]; then
        platforms="${platforms}
    telegram:
      enabled: true"
        if [ -n "${TELEGRAM_HOME_CHAT_ID:-}" ]; then
            platforms="${platforms}
      home_channel: \"${TELEGRAM_HOME_CHAT_ID}\""
        fi
    fi

    if [ "${WHATSAPP_ENABLED:-false}" = "true" ]; then
        platforms="${platforms}
    whatsapp:
      enabled: true"
    fi

    if [ -n "${DISCORD_BOT_TOKEN:-}" ]; then
        platforms="${platforms}
    discord:
      enabled: true"
    fi

    if [ -n "${SLACK_BOT_TOKEN:-}" ]; then
        platforms="${platforms}
    slack:
      enabled: true"
    fi

    cat > "$config_file" <<YAML
# Hermes Agent Configuration
# Generated by hermes-umbrel entrypoint

model:
  default: "${model}"
  provider: "${provider}"

gateway:
  streaming: true
  platforms:${platforms:-"
    # No platforms configured"}

session_reset:
  mode: both
  at_hour: 4
  idle_minutes: 1440

update:
  checkOnStart: false

context_compression:
  enabled: true

memory:
  enabled: true
YAML

    chmod 600 "$config_file"
    echo "Config generated at $config_file"
else
    echo "Config exists at $config_file, using existing"
fi

# ── Activate venv and start ──────────────────────────────────────────────────
# shellcheck disable=SC1091
source /app/venv/bin/activate

# ── Enable the OpenAI-compatible API server on all gateway instances ──────────
# This allows the dashboard (setup container) to chat with profiles and enables
# inter-agent communication via the ask-agent skill.
export API_SERVER_ENABLED=true
export API_SERVER_HOST=0.0.0.0

case "${1:-gateway}" in
    gateway)
        # Auto-start any profile gateways that were previously running
        if [ -d "$HERMES_HOME/profiles" ]; then
            for profile_dir in "$HERMES_HOME/profiles"/*/; do
                [ ! -d "$profile_dir" ] && continue
                profile_name=$(basename "$profile_dir")
                state_file="$profile_dir/gateway_state.json"
                if [ -f "$state_file" ]; then
                    prev_state=$(python3 -c "import json; print(json.load(open('$state_file')).get('gateway_state',''))" 2>/dev/null)
                    if [ "$prev_state" = "running" ]; then
                        echo "Auto-starting profile gateway: $profile_name"
                        HERMES_HOME="$profile_dir" hermes gateway run &
                        sleep 1
                    fi
                fi
            done
        fi

        echo "Starting Hermes gateway (default)..."
        # Use 'run' (foreground) not 'start' (systemctl) — no systemd in Docker
        exec hermes gateway run
        ;;
    shell)
        echo "Starting shell..."
        exec /bin/bash
        ;;
    *)
        exec "$@"
        ;;
esac
